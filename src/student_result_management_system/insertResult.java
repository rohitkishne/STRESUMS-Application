    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        String jTextField;
        // TODO add your handling code here:
        String rollno = jTextField1.getText();
        String phy = jTextField2.getText();
        String chem = jTextField3.getText();
        String math = jTextField4.getText();
        String em = jTextField5.getText();
        String dbms = jTextField6.getText();
        
        try{
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srmdb","root","Panthera123@");
            
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            
            if(rs.next())
            {
                st.executeUpdate("insert into result values('"+rollno+"','"+phy+"','"+chem+"','"+math+"','"+em+"','"+dbms+"')");
                JOptionPane.showMessageDialog(null, "Data has been Successfully Updated");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Entered Roll Number is incorrect");
            }
            
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null, e.toString());
            
        }
    }//GEN-LAST:event_jButton5ActionPerformed

